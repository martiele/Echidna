<?php
namespace applications\login\entities;

use core\db\Field;
class User extends \core\Model{
    static function getTable()
    {
        return 'users'; // TODO: Change the autogenerated stub
    }


    static function schema()
    {
        return [
            "id"        =>  Field::primaryIndex(),
            "username"  =>  Field::varchar(20)->editable(),
            "password"  =>  Field::varchar(128)->editable(),
            "email"     =>  Field::varchar(128)->editable(),
            "created"   =>  Field::date(),
            "last_modified" =>  Field::date(),
            "type"      =>  Field::int()->editable(),
            "active"    =>  Field::int(),
            "nome"      =>  Field::varchar(256),
            "cognome"   =>  Field::varchar(256),
            "data_di_nascita"   =>  Field::date()->editable(),
            "profile_img"   =>  Field::int()->editable(),
            "storia"        =>  Field::text()->editable()
        ];
    }

}