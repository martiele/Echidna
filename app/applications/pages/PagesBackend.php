    static function actionContentOrder( $get = [],$post){

        $smtp = null;
        foreach ($post as $key => $item) {
            $smtp = Db::$connection->prepareWithValues('UPDATE contenuto set ordine = :ordine WHERE id= :id',
                [
                    "ordine"    =>  $key,
                    "id"        =>  $item
                ]);
            $smtp->execute();
        }


        Cache::clear(Contenuto::class);


        exit;
        return ["","data"=>true];
    }

